package com.arek.database_utils;

public class WordAndTranslation {

    String word;
    String translation;

    public WordAndTranslation(String word, String translation){
        this.word = word;
        this.translation = translation;
    }

    public String getWord() {
        return word;
    }

    public String getTranslation() {
        return translation;
    }

}
